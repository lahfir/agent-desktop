//! C-boundary validation for every `#[repr(i32)]` enum exposed to C.
//!
//! A C caller can write an arbitrary `int32_t` into the enum-typed field
//! of a `#[repr(C)]` struct. Reading that field as the Rust enum type
//! and pattern-matching against its variants triggers **undefined
//! behavior** when the discriminant is out-of-range — Rust is allowed
//! to assume enum values are always valid.
//!
//! The pattern in this module avoids that UB entirely:
//!
//! 1. Never read the field as the enum type. Instead, read the raw
//!    `i32` bytes via `enum_raw_i32`, which does a byte-level memcpy
//!    through a pointer cast — always defined behavior.
//! 2. Convert the raw `i32` back into the enum via `<Enum>::from_c`
//!    (generated by the `try_from_c_enum!` macro). The generated match
//!    only yields `Some(Variant)` for known-good discriminants; an
//!    unknown bit pattern returns `None` and the caller returns
//!    `AD_RESULT_ERR_INVALID_ARGS`.
//!
//! The field-type stays the enum so cbindgen keeps emitting the strongly
//! typed C signatures — the memory layout is identical to `int32_t`
//! thanks to `#[repr(i32)]`.

use crate::types::{
    AdActionKind, AdDirection, AdImageFormat, AdModifier, AdMouseButton, AdMouseEventKind,
    AdScreenshotKind, AdSnapshotSurface, AdWindowOpKind,
};

/// Reads the raw `i32` discriminant out of an `#[repr(i32)]` enum field
/// without invoking the enum's validity invariant. Safe because
/// `#[repr(i32)]` guarantees the bit layout matches `i32`.
pub(crate) fn enum_raw_i32<T>(field: &T) -> i32 {
    debug_assert_eq!(std::mem::size_of::<T>(), std::mem::size_of::<i32>());
    unsafe { std::ptr::read(field as *const T as *const i32) }
}

macro_rules! try_from_c_enum {
    ($ty:ty { $($variant:ident = $val:expr),+ $(,)? }) => {
        impl $ty {
            #[allow(dead_code)] // not every enum is read from C today; keep the validator uniformly
            pub(crate) fn from_c(raw: i32) -> Option<Self> {
                match raw {
                    $($val => Some(<$ty>::$variant),)+
                    _ => None,
                }
            }
        }
    };
}

