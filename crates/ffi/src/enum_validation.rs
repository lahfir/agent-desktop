#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_action_kind_valid_range() {
        for raw in 0..=20 {
            assert!(AdActionKind::from_c(raw).is_some(), "raw = {}", raw);
        }
    }

    #[test]
    fn test_action_kind_out_of_range() {
        assert!(AdActionKind::from_c(-1).is_none());
        assert!(AdActionKind::from_c(21).is_none());
        assert!(AdActionKind::from_c(999).is_none());
        assert!(AdActionKind::from_c(i32::MAX).is_none());
        assert!(AdActionKind::from_c(i32::MIN).is_none());
    }

    #[test]
    fn test_direction_valid_range() {
        for raw in 0..=3 {
            assert!(AdDirection::from_c(raw).is_some());
        }
        assert!(AdDirection::from_c(4).is_none());
    }

    #[test]
    fn test_modifier_valid_range() {
        for raw in 0..=3 {
            assert!(AdModifier::from_c(raw).is_some());
        }
        assert!(AdModifier::from_c(-5).is_none());
    }

    #[test]
    fn test_mouse_button_valid_range() {
        assert!(AdMouseButton::from_c(0).is_some());
        assert!(AdMouseButton::from_c(2).is_some());
        assert!(AdMouseButton::from_c(3).is_none());
    }

    #[test]
    fn test_mouse_event_kind_valid_range() {
        assert!(AdMouseEventKind::from_c(0).is_some());
        assert!(AdMouseEventKind::from_c(3).is_some());
        assert!(AdMouseEventKind::from_c(4).is_none());
    }

    #[test]
    fn test_window_op_kind_valid_range() {
        assert!(AdWindowOpKind::from_c(0).is_some());
        assert!(AdWindowOpKind::from_c(4).is_some());
        assert!(AdWindowOpKind::from_c(5).is_none());
    }

    #[test]
    fn test_screenshot_kind_valid_range() {
        assert!(AdScreenshotKind::from_c(0).is_some());
        assert!(AdScreenshotKind::from_c(2).is_some());
        assert!(AdScreenshotKind::from_c(3).is_none());
    }

    #[test]
    fn test_image_format_valid_range() {
        assert!(AdImageFormat::from_c(0).is_some());
        assert!(AdImageFormat::from_c(1).is_some());
        assert!(AdImageFormat::from_c(2).is_none());
    }

    #[test]
    fn test_enum_raw_round_trip() {
        let k = AdActionKind::Scroll;
        let raw = enum_raw_i32(&k);
        assert_eq!(raw, 12);
        let back = AdActionKind::from_c(raw).unwrap();
        assert!(matches!(back, AdActionKind::Scroll));
    }

    #[test]
    fn test_fuzz_no_panic() {
        // Any raw i32, even crafted bit patterns, must never panic.
        for raw in [-1_000_000_i32, -1, 0, 1, 20, 21, 999, i32::MAX, i32::MIN] {
            let _ = AdActionKind::from_c(raw);
            let _ = AdDirection::from_c(raw);
            let _ = AdModifier::from_c(raw);
            let _ = AdMouseButton::from_c(raw);
            let _ = AdMouseEventKind::from_c(raw);
            let _ = AdWindowOpKind::from_c(raw);
            let _ = AdScreenshotKind::from_c(raw);
            let _ = AdImageFormat::from_c(raw);
        }
    }
}
